int main() {
    int day = 3;
    string dayy;
    
    switch(day) {
        case 1:
            dayy = "Monday";
            break;
        case 2:
            dayy = "Tuesday";
            break;
        case 3:
            dayy = "Wednesday";
            break;
        default:
            dayy = "Invalid day";
    }
    
    return 0;
}